// Overwriting Variables
// Set the variable 'coding' to "weird"
let coding = "weird";

// Print the variable 'coding' to your console
console.log("coding is " + coding);     // -> "coding is weird"

// Set 'coding' to "cool and fun"
coding = "cool and fun";

// Print the variable 'coding' to your console again
console.log("coding is " + coding);     // -> "coding is cool and fun"
